export const messages = {
  "en-US": en,
  "zh-CN": zh,
} as const;

export type MessageKey = keyof typeof zh;

export function normalizeLocale(value: unknown): Locale {
  const raw = String(value ?? "").trim().toLowerCase();
  return raw.startsWith("en") ? "en-US" : "zh-CN";
}

export function t(locale: unknown, key: MessageKey | string, params: I18nParams = {}) {
  const normalized = normalizeLocale(locale);
  const template = (messages[normalized] as Record<string, string>)[key] ?? (messages["zh-CN"] as Record<string, string>)[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(params[name] ?? ""));
}
